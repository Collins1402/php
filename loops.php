<h1>Loops</h1>

<?php
// while loop

$min = 0;
while($min < 10){
    print $min . "<br>";
    $min++;
}
?>

<h4>do-while Loop</h4>
<?php
    // do while loop
    $x = 1;
    do{
    print $x . "<br>";
    $x++;
   }while($x < 10);
    
?>
<h4> for loop</h4>
<?php
    // for loop
    for($r = 3; $r < 12; $r++){
        print $r . "<br>";
    }

?>


